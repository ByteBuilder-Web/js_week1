// In javascript there are 5 types of operator
// 1. Arithmetic Operator
// 2. Assignment Operator
// 3. Comparison Operator
// 4. Logical Operator

// 1. Arithmetic Operator
let a = 10;
let b = 20;
console.log(a + b);

// 2. Assignment Operator
let c = 10;
let d = 20;
 c = d;
console.log(c);

// 3. Comparison Operator

//Comparing only value
let e = 10;
let f = 20;
console.log(e == f); 

// comparing both value and datatype
let p = 23; 
let w = 23; 
console.log(p === w);